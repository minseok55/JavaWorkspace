package chap_01;

public class _02_DataTypes { // 자료형
    public static void main(String[] args) { //  main메소드(영역), psvm 또는 main
        System.out.println(); // sout 자동완성
        // 프로그램 코드는 위에서 아래방향으로 실행
        System.out.println("Hello World"); // 문자열 출력
        System.out.println("안녕하세요?");
        System.out.println(12); // 양의 정수 / 숫자 자료형
        System.out.println(-34); // 음의 정수
        System.out.println(3.14); // 실수
        System.out.println(true); // 참, 거짓 / boolean 자료형
        System.out.println(false);

        System.out.println(123 + 345); // 자동으로 더하기 연산
        // 가독성을 높여주기 위해서 들여쓰기 띄어쓰기 사용
    }
}
